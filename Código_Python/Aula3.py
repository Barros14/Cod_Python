"""
DocStrings
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Gilson Barros')
print(1, 'Gilson "Barros"')

# Aspas duplas

print("Gilson Barros")
print(2, "Gilson 'Barros'")


# Escape
print("Gilson \"Barros\"")

# r
print(r"Gilson \"Barros")
